package Sept20;

//자바파일의이름=클래스의이름
class BooleanEx{
	//가상머신에서 클래스를 실행할때 가장먼저 호출하는 메서드.		
	public static void main(String[] args)
	//	 Main Method Program 실행 시 Main Method에서 부터 시작된다.
	{
		
		boolean b = true; 		
		//변수선언방법 : 자료형 변수명 = 변수값 
		
		System.out.println("변수 b의 값 :" +b);
		
	}
}

/*
{}:코드의 블럭(영역)을 설정.
():메서드,생성자
[]:배열
<>:제네릭
*/